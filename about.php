            <p><strong>Today,</strong> Mark teaches the Word of God (in Poland, Portugal, and the USA), and Tami is often asked to teach at women's meetings.  They fellowship at <a href="http://www.montereychapel.org/" target="_blank">Monterey Bible Chapel</a>, in the Lancaster, PA area.  Their focus as Christians is to preach the <a href="http://www.bible.org/page.asp?page_id=276" target="_blank">gospel</a>, teach the Word of God, and practice Christ-centered Christianity.</p>
