            <h1 class="title">About Us</h1>
            <p>Mark and Tami were born and raised in the USA.  They are Christians, which means that they are followers of the Lord Jesus Christ.  They are associated with the <a href="christian-resources.php#who_are_the_brethren">the open brethren movement</a>, a group of local churches around the world that has no central organization.</p>
