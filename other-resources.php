            <h1>The Gospel</h1>
            <p>The Bible says that we are all sinners.  No one does what God considers right.  Only God Himself is perfectly right in all He does.  In fact, He is completely separate from sin.  He is too Holy to tolerate sin.  According to God's law, the result of sin is death.  Because you sin, you deserve death.  Do not think that God will choose to overlook your sin.  Little as it may be, God is perfectly just.  He will punish all sin.  However, God is also perfect in love and mercy.  He will have justice, but He will also have mercy.  God's law says that the punishment for sin must be death, and the punishemnt must be paid.  But God himself sent His Son to the Earth in the form of a man to die to pay the punishment for our sins.  The punishement of sin is death.  We sinned, and we deserved to die.  God's Son did not sin, and He did not deserve to die.  We sinned, and God's Son chose to die.  Then, He rose from the grave, and now He offers salvation from sin to all who are willing to receive His free gift.  How can we receive God's forgiveness of sin?  We cannot attain salvation through our good works.  The punishment for sin is death, not the obligation to do good.  The Bible says that the only way to be saved is to believe on God's Son, Jesus.  Admit your sin, and receive Jesus.  Put faith in Him &mdash; not a dead faith but a living faith that is willing to take action &mdash; and God will save you.  He will not only <em>forgive</em> you from sin, but He will <em>save</em> you from it.  We cannot break free of the power of sin, but God will free us from sin so that we don't have to continue living in sin.  He will live inside us and keep us from sin.  He will give us a new desire for what He desires rather than for sin.</p>

