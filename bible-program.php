            <h2>The Church</h2>

            <ul>
                <li id="universal-church">
                    The Global Church
                    <ul>
                        <li>The Scriptures as the only guide</li>
                        <li>The Church is separate from the world</li>
                        <li>The unity of the Church</li>
                        <li>The head of the Church</li>
                        <li>The purpose of the Church</li>
                        <li>
                            The Pilgrim Church: Christ centered, Bible Focused, Pilgrim in character
                            <ul>
                                <li>Tracing the history of such movements from the 1<sup>st</sup> century</li>
                            </ul>
                        </li>
                    </ul>
                </li>
                <li id="local-church">
                    The Local Church
                    <ul>
                        <li>
                            What is a biblical or New Testament assembly?
                            <ul>
                                <li>Principles found in Acts 2:42</li>
                                <li>Gathering to be with Christ</li>
                                <li>Autonomy and interdependency</li>
                                <li>Christ-centered churches</li>
                            </ul>
                        </li>
                        <li>
                            Teaching in the assembly (the apostle's doctrine)
                            <ul>
                                <li>The principle is found throughout the New Testament</li>
                            </ul>
                        </li>
                        <li>
                            Fellowship with Christ and with one another (The Table of teh Lord)
                            <ul>
                                <li>Relationship between local churchs (Unity and Separation)</li>
                                <li>What the New Testament says about fellowship</li>
                            </ul>
                        </li>
                        <li>
                            Worship and the Lord's Supper (The Breaking of Bread)
                            <ul>
                                <li>Principles of worship</li>
                                <li>The symbols of the bread and cup</li>
                            </ul>
                        </li>
                        <li>
                            Prayer and the assembly (Dependency on Christ)
                            <ul>
                                <li>What the New Testament teaches about Prayer and Fasting</li>
                            </ul>
                        </li>
                        <li>
                            The headship of Christ (the authority of the local assembly)
                            <ul>
                                <li>Christ is the ultimate authority</li>
                                <li>The symbols of headship (distinct roles)</li>
                            </ul>
                        </li>
                        <li>
                            Priesthood of all believers (and the gifts of the Spirit) and the role of men and women
                            <ul>
                                <li>Everything in an assembly should not be done by only one or a few men</li>
                                <li>
                                    The practice of the priesthood of all believers should be evident in all our gatherings:
                                    <ul>
                                        <li>Multiple teachers/participation</li>
                                        <li>Active and true fellowship with all</li>
                                    </ul>
                                </li>
                                <li>
                                    Priesthood extends to all believers, not just men
                                    <ul>
                                        <li>
                                            Thus, given the roles within the assembly, we are to find ways to encourage men and women to use their gifts in and out of the assembly in practical ways so it can be evident that the assembly is vibrant and all within and without can see that each member is important and contributing in some way to the assembly.
                                        </li>
                                    </ul>
                                </li>
                                <li>
                                    What is the role of a male priest
                                    <ul>
                                        <li>(including how to prepare sermons or lead a Bible study)</li>
                                    </ul>
                                </li>
                                <li>
                                    What is the role of a female priest
                                </li>
                            </ul>
                        </li>
                        <li>
                            Plurality of Elders and Deacons (Servant/humble leadership)
                            <ul>
                                <li>Different leadership styles in Church history</li>
                                <li>The challlenge and responsibility of passing responsibiligy to the next generation</li>
                            </ul>
                        </li>
                        <li>
                            Importance of Evangelism and Missions (The command to GO)
                            <ul>
                                <li>The treasure that believers have (2 Cor 4:7)</li>
                                <li>Good works and the church (serving others)</li>
                                <li>The call of the church to help the poof, the outcast, the oppressed (to exhibit the character of Christ in this world)</li>
                                <li>Fishing in new places (Finding new opportunities to witness for Christ)</li>
                            </ul>
                        </li>
                        <li>Assembly Discipline (Ministry of reconciliation)</li>
                        <li>Baptism (As symbol of salvation, indentification, and union with Christ)</li>
                        <li>
                            Revival and the lessons of History
                            <ul>
                                <li>The need for revival (4 generations)</li>
                                <li>Personal revival</li>
                                <li>Revival in the Word of God</li>
                                <li>Characteristics of Biblical revival</li>
                            </ul>
                        </li>
                        <li>
                            The problem with generationss
                            <ul>
                                <li>Principles of gathering cannot change.  We have no right to change them.  But style, culture, circumstances, structures do change and will change.</li>
                                <li>Principles do not answer the question of style or structure &endash what time our meetings should be or how to arrange the meetings or how long they should be.  They do not tell us whether we should have a Sunday school or midweek meeting or musical instraments or type of music.</li>
                                <li>Cultral issues affect the church and need to be addressed: i.e. marriage, the rist of Post-Modern generation, Science, changes in the Law, etc.  Principles need to survive, not our buildings, tax status, or current strugures.</li>
                                <li>The assembly/local church is not a socrial club.  It is a body, a living organism.</li>
                                <li>Often the battle is not over doctrine, but over style and structure.  Should there be a traditional style or a progressive style?  This has nothing to do with doctrine.  There is a need for strong and good teaching in theis area so that our gatherings are based on unchanging biblical principles, and there is freedom in style and structure.</li>
                            </ul>
                        </li>
                        <li>
                            The problem with human institutions/organizations
                            <ul>
                                <li>Men try to control the assembly</li>
                                <li>Organizations do the worlk that the assembly is responsible to do</li>
                                <li>The difference between biblical and political ecumenicalism</li>
                            </ul>
                        </li>
                    </ul>
                </li>
