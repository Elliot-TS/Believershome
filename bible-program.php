            <h1>Introduction</h1>

            <ul>
                <!-- The Purpose -->
                <li>
                    The Purpose is to strengthen assemblies and believers by
                    <ul>
                        <li>Preaching Christ and the Gospel to saved and unsaved alike</li>
                        <li>
                            Building up believers by teaching
                            <ul>
                                <li>The fundamentals of the Christian faith</li>
                                <li>The fundamentals of Christian living</li>
                                <li>The Word of God</li>
                            </ul>
                        </li>
                        <li>Teaching Principles that Characterize a healthy assembly</li>
                    </ul>
                </li>

                <!-- The Goal -->
                <li>
                    The Goal
                    <ul>
                        <li>To see unbelievers come to faith in Christ</li>
                        <li>To produce mature, fruitful believers who have a deep love for Christ and the Word of God</li>
                        <li>To produce healthy, biblical assemblies that are Christ-centered, Bible-focused, and Pilgrim in character</li>
                    </ul>
                </li>

                <!-- The Program Structure -->
                <li>
                    The Program Structure
                    <ul>
                        <li>
                            This is only a guide.
                            <ul>
                                <li>The program can be adjusted or tweaked to meet various needs.</li>
                                <li>The material presented here is material that each generation is responsible to teach and practice</li>
                                <li>It ought to be the responsibility of the local assembly to pass on "the faith that was once for all entrusted to the saints." (Jude 3 Net)</li>
                                <li>Mature believers should find ways to disciple younger believers in these truths.</li>
                                <li>At times, assemblies ought to co-operate together to effectively pass on these truths.</li>
                            </ul>
                        </li>
                        <li>
                            Recommendations
                            <ul>
                                <li>Organize the program 5-7 times per year.</li>
                                <li>Each program should have 3-5 sessions AND a session for questions/comments</li>
                                <li>The programs should have a casual and informal atmosphere with food provided.</li>
                                <li>The teaching style depends on the teacher (lecture, interactive, inductive, visual aids)</li>
                                <li>Each program can have one teacher or a plurality of teachers who are able to connect to the audience and not just teach.</li>
                                <li>Sessions could be broken up into smaller seminars running at the same time (this would require more than one teacher)</li>
                            </ul>
                        </li>
                        <li>
                            There is enough material here for about 10-15 years.
                            <ul>
                                <li>There are important topics <strong>NOT</strong> on this list.  Some topics would be taught as they come up in the Word of God.</li>
                            </ul>
                        </li>
                        <li>
                            What seems to work in our generation and what we learned in Krakow is that
                            <ul>
                                <li>Teachers and organizers need to build relationships with the students.  This generation is a relational generation.  It is important not just to teach, but to show care and listen to students.</li>
                                <li>Millenials, or Generation Y (born between 1981 and 2000), are more receptive than previous generations to learning that exists as a function of meaningful relationship.</li>
                                <li>The students were happy to be able to ask questions and get answers to their questions about Christianity</li>
                                <li class="caution" title="not parallel with other points">Continual reminders of upcoming meetings via email, phone, and personal encouragement</li>
                            </ul>
                        </li>
                    </ul>
                </li>

            </ul>
